# ── Answer prompt with retries ───────────────────────────────────

prompt_answer() {
    local correct="$1"
    local attempts=0
    local max_attempts=3

    while [ $attempts -lt $max_attempts ]; do
        echo -ne "${CYAN}Your answer (A/B/C/D): ${RESET}"
        read -r answer
        answer=$(echo "$answer" | tr '[:lower:]' '[:upper:]' | tr -d '[:space:]')

        if [[ "$answer" != "A" && "$answer" != "B" && "$answer" != "C" && "$answer" != "D" ]]; then
            echo -e "${RED}Invalid input. Enter A, B, C, or D.${RESET}"
            continue
        fi

        if [ "$answer" == "$correct" ]; then
            echo -e "${GREEN}CORRECT! +1000 aura${RESET}"
            score=$((score + 1))
            sleep 1
            return 0
        else
            attempts=$((attempts + 1))
            remaining=$((max_attempts - attempts))
            if [ $remaining -gt 0 ]; then
                echo -e "${RED}WRONG! -500 aura. $remaining attempts remaining.${RESET}"
            else
                echo -e "${RED}WRONG! The answer was $correct.${RESET}"
                echo -e "${RED}Tralalero Tralala is disappointed in you.${RESET}"
                sleep 2
                return 1
            fi
        fi
    done
}

# ── Quiz logic ───────────────────────────────────────────────────

run_quiz() {
    score=0

    # Shuffle order
    local order=(0 1 2 3)
    for ((i=${#order[@]}-1; i>0; i--)); do
        j=$((RANDOM % (i+1)))
        tmp=${order[$i]}
        order[$i]=${order[$j]}
        order[$j]=$tmp
    done

    local question_num=1
    for idx in "${order[@]}"; do
        clear
        show_banner
        echo -e "${WHITE}Character $question_num/4:${RESET}"
        echo ""

        case $idx in
            0) ask_tungtung ;;
            1) ask_tralalero ;;
            2) ask_trippi ;;
            3) ask_bombardiro ;;
        esac

        question_num=$((question_num + 1))
    done

    clear
    if [ $score -eq 4 ]; then
        echo -e "${GREEN}"
        cat << 'ART'

  __   __            ____                        _ _
  \ \ / /__  _   _  |  _ \ __ _ ___ ___  ___  __| | |
   \ V / _ \| | | | | |_) / _` / __/ __|/ _ \/ _` | |
    | | (_) | |_| | |  __/ (_| \__ \__ \  __/ (_| |_|
    |_|\___/ \__,_| |_|   \__,_|___/___/\___|\__,_(_)

    BOMBARDIRO CERTIFIED - TERMINAL UNLOCKED
    Your aura: INFINITE

ART
        echo -e "${RESET}"
        sleep 2
    else
        echo -e "${RED}"
        cat << 'ART'

    ___________________________________________
   |                                           |
   |  TUNG TUNG TUNG - YOU HAVE BEEN BONKED   |
   |                                           |
   |     You got $score/4. You need 4/4.       |
   |                                           |
   |     Back to the shadow realm...           |
   |___________________________________________|

ART
        echo -e "${RESET}"
        echo -e "${YELLOW}Restarting quiz in 3 seconds...${RESET}"
        sleep 3
        run_quiz
    fi
}

